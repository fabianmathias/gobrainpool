package gobrainpool

// Curve is an opaque handle to one of the Brainpool curves defined in
// RFC 5639. It is constructed only by the package — use [BP256r1],
// [BP384r1] or [BP512r1] to obtain the singleton for each curve.
//
// The zero value is not usable. Curve equality is pointer equality:
// two *Curve values refer to the same curve iff they are the same
// singleton.
type Curve struct {
	name     string
	pBE      []byte // big-endian P, length = byteSize
	nBE      []byte // big-endian N, length = byteSize
	bitSize  int
	byteSize int
}

// Name returns the standard curve name (e.g., "brainpoolP256r1").
func (c *Curve) Name() string { return c.name }

// BitSize returns the size of the underlying field in bits.
func (c *Curve) BitSize() int { return c.bitSize }

// ByteSize returns the size of the curve's field elements and scalars in
// bytes — equivalent to ceil(BitSize/8). Fixed-width scalars and the X/Y
// coordinates of an uncompressed SEC1 point are each this many bytes long.
func (c *Curve) ByteSize() int { return c.byteSize }

// Curve parameters are stored as fixed-width big-endian byte literals
// rather than derived from math/big at package init. This matches the
// pattern used by crypto/internal/fips140: the FIPS cryptographic
// boundary does not execute math/big code. Byte literals for pBE and
// nBE are pre-computed from the hex values in RFC 5639 §3 and exercised
// against the RFC 7027 known-answer vectors by the test suite — if a
// byte is wrong, KAT will fail.

